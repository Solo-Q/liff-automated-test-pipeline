export async function generateTests(context, logger) {
  const { repoPath, options } = context;
  logger.info("Stage started: generate", {
    stage: "generate",
    repoPath,
    dryRun: options.dryRun,
  });

  const inputs = await loadGeneratorInputs(context, logger);
  if (!inputs.ok) {
    return {
      stage: "generate",
      ok: false,
      exitCode: inputs.exitCode,
      message: inputs.message,
      errorCode: inputs.errorCode,
      data: inputs.data,
    };
  }

  const { projectProfile, featureMap } = inputs.data;
  if (!projectProfile.usesLiff) {
    return {
      stage: "generate",
      ok: false,
      exitCode: EXIT_CODES.UNSUPPORTED_PROJECT,
      message: "Test generation requires a LIFF repository.",
      errorCode: "LIFF_NOT_DETECTED",
      data: inputs.data,
    };
  }

  const generationPlan = buildGenerationPlan(featureMap, projectProfile);
  const mutations = [];

  mutations.push({
    path: ".liff-testgen/generation-plan.json",
    action: (await pathExists(path.join(repoPath, ".liff-testgen/generation-plan.json"))) ? "update" : "create",
  });

  if (!options.dryRun) {
    const stateDir = path.join(repoPath, ".liff-testgen");
    await ensureDirectory(stateDir);
    await writeJsonFile(path.join(stateDir, "generation-plan.json"), generationPlan);
  }

  const generatedFileMutations = await ensureGeneratedFiles(repoPath, generationPlan, projectProfile, options.dryRun);
  mutations.push(...generatedFileMutations);

  const pipelineStatePath = path.join(repoPath, ".liff-testgen/pipeline-state.json");
  const pipelineState = await buildPipelineState(pipelineStatePath, mutations, options.dryRun);

  if (!options.dryRun) {
    await writeJsonFile(pipelineStatePath, pipelineState);
  }

  return {
    stage: "generate",
    ok: true,
    exitCode: EXIT_CODES.SUCCESS,
    message: "Test generation completed.",
    data: {
      dryRun: options.dryRun,
      projectProfile,
      featureMap,
      generationPlan,
      mutations,
      pipelineState,
    },
  };
}

async function loadGeneratorInputs(context, logger) {
  const repoPath = context.repoPath;
  const profilePath = path.join(repoPath, ".liff-testgen/project-profile.json");
  const featureMapPath = path.join(repoPath, ".liff-testgen/feature-map.json");

  if ((await pathExists(profilePath)) && (await pathExists(featureMapPath))) {
    return {
      ok: true,
      exitCode: EXIT_CODES.SUCCESS,
      data: {
        projectProfile: await readJsonFile(profilePath),
        featureMap: await readJsonFile(featureMapPath),
      },
    };
  }

  const extractResult = await extractFeatures(context, logger);
  if (!extractResult.ok) {
    return extractResult;
  }

  return {
    ok: true,
    exitCode: EXIT_CODES.SUCCESS,
    data: {
      projectProfile: await resolveProjectProfile(repoPath, context, logger, extractResult),
      featureMap: extractResult.data.featureMap,
    },
  };
}

async function resolveProjectProfile(repoPath, context, logger, extractResult) {
  const profilePath = path.join(repoPath, ".liff-testgen/project-profile.json");
  if (await pathExists(profilePath)) {
    return readJsonFile(profilePath);
  }

  if (extractResult?.data?.projectProfile) {
    return extractResult.data.projectProfile;
  }

  const analysis = await analyzeRepository(context, logger);
  return analysis.data.projectProfile;
}

function buildGenerationPlan(featureMap, projectProfile) {
  const plan = [];

  for (const feature of featureMap) {
    const levels = Array.isArray(feature.recommendedTests) ? feature.recommendedTests : [];
    const scenarioPool = buildScenarioPool(feature);
    for (const level of levels) {
      if (level === "e2e" && projectProfile.e2eFramework !== "playwright") {
        continue;
      }

      const scenarios = scenarioPool.filter((scenario) => scenario.levels.includes(level));
      for (const scenario of scenarios) {
        const priority = assignPriority(feature, scenario, level);
        const failureMode = inferFailureMode(feature, scenario);
        const testData = buildScenarioTestData(feature, scenario);
        const manualReferenceIds = buildManualReferenceIds(feature, scenario);
        const generationMode = assignGenerationMode(feature, scenario, level, priority, failureMode, manualReferenceIds);
        plan.push({
          id: `${feature.featureId}-${level}-${scenario.scenarioId}`,
          featureId: feature.featureId,
          featureName: feature.name,
          sourceFile: feature.files?.[0] ?? null,
          targetType: feature.targetType ?? TARGET_TYPES.GENERIC,
          moduleType: feature.moduleType ?? null,
          parentFeatureId: feature.parentFeatureId ?? null,
          dependsOnLiff: Boolean(feature.dependsOnLiff),
          level,
          priority,
          generationMode,
          scenarioId: scenario.scenarioId,
          scenarioType: scenario.scenarioType,
          scenarioName: scenario.name,
          scenarioCategory: scenario.category,
          failureMode,
          title: buildScenarioTitle(feature.name, level, scenario),
          fixture: scenario.fixture,
          outputPath: buildOutputPath(feature.featureId, level, feature.targetType ?? TARGET_TYPES.GENERIC),
          featureGoal: buildFeatureGoal(feature),
          successCriteria: buildSuccessCriteria(feature),
          preconditions: scenario.preconditions,
          userActions: scenario.userActions,
          systemResult: scenario.systemResult,
          expectedOutcome: scenario.expectedOutcome,
          testData,
          manualReferenceIds,
          allocationReason: buildAllocationReason(level),
          testQuestion: buildLevelQuestion(level),
        });
      }
    }
  }

  return plan;
}

function buildScenarioTitle(featureName, level, scenario) {
  return `${featureName} ${level} ${scenario.scenarioId} ${scenario.name}`;
}

function buildFeatureGoal(feature) {
  if (feature.dependsOnLiff) {
    return `${feature.name} should complete a LIFF-dependent user flow safely.`;
  }
  return `${feature.name} should complete its primary user flow correctly.`;
}

function buildSuccessCriteria(feature) {
  const criteria = [
    `${feature.name} renders or completes without unexpected errors.`,
  ];
  if (feature.dependsOnLiff) {
    criteria.push(`${feature.name} handles LIFF state and user context correctly.`);
  }
  if (feature.risk !== "low") {
    criteria.push(`${feature.name} preserves safe behavior under fallback or edge conditions.`);
  }
  return criteria;
}

function buildAllocationReason(level) {
  if (level === "unit") {
    return "Validate feature rules, state transitions, and local rendering decisions.";
  }
  if (level === "integration") {
    return "Validate page, state, router, API, and LIFF mocks working together.";
  }
  return "Validate that the user journey can complete through the main entry path.";
}

function buildLevelQuestion(level) {
  if (level === "unit") {
    return "Is the feature logic itself correct?";
  }
  if (level === "integration") {
    return "Do the integrated modules run correctly together?";
  }
  return "Can the user complete the core journey?";
}

function buildOutputPath(featureId, level, targetType = TARGET_TYPES.GENERIC) {
  if (level === "e2e") {
    return `tests/generated/e2e/${featureId}.spec.ts`;
  }
  const extension = usesJsxTemplate(targetType) ? "tsx" : "ts";
  return `tests/generated/${level}/${featureId}.${level}.test.${extension}`;
}

function usesJsxTemplate(targetType) {
  return targetType === TARGET_TYPES.ROUTE_GUARD ||
    targetType === TARGET_TYPES.AUTH_ENTRY_FLOW ||
    targetType === TARGET_TYPES.CARD_SHARE_FLOW_PAGE ||
    targetType === TARGET_TYPES.SHARE_WORKFLOW_PAGE ||
    targetType === TARGET_TYPES.MYCASH_PAGE_FLOW ||
    targetType === TARGET_TYPES.CARD_MANAGE_PAGE_FLOW ||
    targetType === TARGET_TYPES.CARD_DETAIL_FLOW_PAGE ||
    targetType === TARGET_TYPES.CARDHOLDER_PAGE_FLOW ||
    targetType === TARGET_TYPES.AUTH_ONBOARDING_PAGE;
}

async function ensureGeneratedFiles(repoPath, generationPlan, projectProfile, dryRun) {
  const plansByOutput = new Map();

  for (const item of generationPlan) {
    const bucket = plansByOutput.get(item.outputPath) ?? [];
    bucket.push(item);
    plansByOutput.set(item.outputPath, bucket);
  }

  const mutations = [];

  for (const [relativePath, items] of plansByOutput.entries()) {
    const absolutePath = path.join(repoPath, relativePath);
    const nextContent = buildGeneratedTestContent(relativePath, items, projectProfile);
    const exists = await pathExists(absolutePath);

    if (!exists) {
      mutations.push({
        path: relativePath,
        action: "create",
      });

      if (!dryRun) {
        await ensureDirectory(path.dirname(absolutePath));
        await writeTextFile(absolutePath, nextContent);
      }
      continue;
    }

    const currentContent = await readTextFile(absolutePath);
    if (!currentContent.includes(TOOL_SIGNATURE) || !currentContent.includes(AUTOGEN_START)) {
      mutations.push({
        path: relativePath,
        action: "skip",
        reason: "Existing generated test is not tool-owned.",
      });
      continue;
    }

    const updatedContent = mergeToolOwnedContent(currentContent, nextContent, {
      MANUAL_START,
      MANUAL_END,
    });
    if (updatedContent === currentContent) {
      mutations.push({
        path: relativePath,
        action: "skip",
        reason: "Generated test is already up to date.",
      });
      continue;
    }

    mutations.push({
      path: relativePath,
      action: "update",
    });

    if (!dryRun) {
      await writeTextFile(absolutePath, updatedContent);
    }
  }

  const staleFileMutations = await cleanupStaleGeneratedFiles(repoPath, plansByOutput, dryRun);
  mutations.push(...staleFileMutations);

  return mutations;
}

async function cleanupStaleGeneratedFiles(repoPath, plansByOutput, dryRun) {
  const expectedPaths = new Set(plansByOutput.keys());
  const generatedFiles = await findFiles(path.join(repoPath, "tests/generated"), ({ relativePath }) => {
    return /\.(test|spec)\.(js|jsx|ts|tsx|mjs|cjs)$/.test(relativePath);
  }).catch(() => []);
  const mutations = [];

  for (const file of generatedFiles) {
    const relativePath = toPosixPath(path.relative(repoPath, file.absolutePath));
    if (expectedPaths.has(relativePath)) {
      continue;
    }

    const currentContent = await readTextFile(file.absolutePath);
    if (!currentContent.includes(TOOL_SIGNATURE)) {
      mutations.push({
        path: relativePath,
        action: "skip",
        reason: "Stale generated test is not tool-owned.",
      });
      continue;
    }

    mutations.push({
      path: relativePath,
      action: "delete",
    });

    if (!dryRun) {
      await deleteFile(file.absolutePath);
    }
  }

  return mutations;
}

function buildGeneratedTestContent(relativePath, items, projectProfile) {
  const level = inferLevelFromOutputPath(relativePath);
  const specializedContent = buildSpecializedTestContent(relativePath, items, projectProfile, level);
  if (specializedContent) {
    return specializedContent;
  }

  const importLines = buildImportLines(relativePath, level, projectProfile);
  const suiteName = items[0]?.featureName ?? items[0]?.featureId ?? relativePath;
  const caseBlocks = indentBlock(items.map((item) => buildCaseBlock(item, level)).join("\n\n"), 2);

  return `${TOOL_SIGNATURE}
${importLines}
describe("${suiteName}", () => {
  beforeEach(() => {
    resetLiffFixture();
    applyLiffFixture("loggedOut");
  });

${AUTOGEN_START}
${caseBlocks}
${AUTOGEN_END}

${MANUAL_START}
// custom cases
${MANUAL_END}
});
`;
}

