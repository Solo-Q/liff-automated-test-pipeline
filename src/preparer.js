function buildApplyFixtureTemplate() {
  return `${TOOL_SIGNATURE}
import { liffFixtures } from "./fixtures";
import { LiffFixtureName, LiffFixtureState, MutableLiffFixtureState } from "./types";

const currentFixtureState: MutableLiffFixtureState = {};

export function applyLiffFixture(name: LiffFixtureName, overrides = {}) {
  Object.assign(currentFixtureState, liffFixtures[name], overrides);
  return currentFixtureState;
}

export function getCurrentLiffFixture(): LiffFixtureState {
  return currentFixtureState;
}
`;
}

function buildResetFixtureTemplate() {
  return `${TOOL_SIGNATURE}
import { getCurrentLiffFixture } from "./applyFixture";

export function resetLiffFixture() {
  const fixture = getCurrentLiffFixture();
  for (const key of Object.keys(fixture)) {
    delete fixture[key];
  }
}
`;
}

function buildTypesTemplate() {
  return `${TOOL_SIGNATURE}
export interface LiffFixtureState {
  isLoggedIn?: boolean
  isInClient?: boolean
  accessToken?: string | null
  idToken?: string | null
  initError?: string | null
  profileError?: string | null
  profile?: {
    userId?: string
    displayName?: string
  } | null
}

export type MutableLiffFixtureState = Record<string, unknown> & LiffFixtureState;

export type LiffFixtureName =
  | "loggedOut"
  | "loggedIn"
  | "profileFailed"
  | "tokenExpired"
  | "initFailed"
  | "notInClient"
  | "permissionDenied";

export type LiffFixtureMap = Record<LiffFixtureName, LiffFixtureState>;
`;
}

function hasDependency(packageJson, dependencyName) {
  const collections = [
    packageJson.dependencies,
    packageJson.devDependencies,
    packageJson.peerDependencies,
    packageJson.optionalDependencies,
  ];

  return collections.some((collection) => Boolean(collection?.[dependencyName]));
}

function sortObjectKeys(input) {
  return Object.fromEntries(Object.entries(input).sort(([left], [right]) => left.localeCompare(right)));
}
